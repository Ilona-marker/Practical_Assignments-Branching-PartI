// C# program to illustrate the use of
// floating-point literals
using System;
namespace MathOperators
{
  class Program 
   {
// Main Method
   public static void Main(String[] args)
  {
// decimal-form literal
double a = 123.456;
// It also acts as decimal literal
double b = 0123.456;
Console.WriteLine(a);
Console.WriteLine(b);
}
}
}
